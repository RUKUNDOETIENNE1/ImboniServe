export type Language = 'en' | 'kin' | 'fr' | 'sw'

export function getTranslation(lang: Language, key: string): string {
  const keys = key.split('.')
  let value: any = translations[lang]
  
  for (const k of keys) {
    value = value?.[k]
  }
  
  return value || key
}

export function translateMenu(menuItem: any, targetLang: Language): any {
  if (targetLang === 'en' || !menuItem) return menuItem
  
  // In production, this would call translation API
  // For now, return original with language marker
  return {
    ...menuItem,
    name: `[${targetLang.toUpperCase()}] ${menuItem.name}`,
    description: menuItem.description ? `[${targetLang.toUpperCase()}] ${menuItem.description}` : ''
  }
}
